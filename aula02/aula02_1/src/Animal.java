public class Animal {
    public String nome;
    public String significadoBom;
    public String significadoRuim;
    public int numero;
    public Animal(String nome, String bom, String ruim, int numero){
        this.nome = nome;
        this.significadoBom = bom;
        this.significadoRuim = ruim;
        this.numero = numero;
    }
}
